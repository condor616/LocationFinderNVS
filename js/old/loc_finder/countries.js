function getCountry(cc) {
	var country = gCountries.find(function(val) {
		return val.code == cc.toUpperCase();
	});
	return country;
}

function sortCountries() {
	gCountries.sort(function(a, b) {
		if(a.name == b.name) {
			return 0;
		}
		var test = new Array(a.name, b.name);
		test.sort();
		if(test[0] == a.name) {
			return -1;
		}
		else {
			return 1;
		}
	});
}